# This is a single-line comment. It starts with the "#" sign.
"""
This is a multi-line comment.
Multi-line comments require triple quotation marks.
"""
# New variables can be declared without having to specify it being a number or string.
number = 5
epic = "Life"
print(number)
print(epic)
# "If" and "else" statements don't need curly brackets.
if number == 4 | number <= 6:
    print(number)
else:
    print("The number is not 4, nor is it less than or equal to 6.")